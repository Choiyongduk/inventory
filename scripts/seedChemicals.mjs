async function run() {
  console.log('기존 약품 데이터를 삭제하는 중...');
  const snap = await getDocs(collection(db, COLLECTION));
  let deleted = 0;
  for (const d of snap.docs) {
    await deleteDoc(doc(db, COLLECTION, d.id));
    deleted += 1;
  }
  console.log(`기존 ${deleted}건 삭제 완료.`);

  console.log(`새 약품 ${ROWS.length}건 등록하는 중...`);
  const batch = writeBatch(db);
  for (const [id, name, purchased, handler, purpose, qty, cat, note] of ROWS) {
    const payload = {
      id,
      name,
      handler: handler || '-',
      purpose: purpose || '-',
      qty: `${qty} EA`,
      cat: cat || '기타',
      purchased,
      opened: '-',
      disposed: addYears(purchased, 3),
      msds: '',
      photo: '',
      storageZone: '',
      hazardClass: '',
      note: note || '',
      updatedAt: new Date(),
    };
    batch.set(doc(db, COLLECTION, `chem_${id}`), payload, { merge: false });
  }
  await batch.commit();
  console.log(`완료! 약품 ${ROWS.length}건이 새로 등록되었습니다.`);
  process.exit(0);
}

run().catch((err) => {
  console.error('오류 발생:', err);
  process.exit(1);
});
